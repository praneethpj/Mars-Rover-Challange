package com.nasa.rover.model;

public class Surface {
    private int upperX;
    private int upperY;

    public Surface(int upperX,int upperY){
        this.upperX=upperX;
        this.upperY=upperY;
    }
    public int getUpperX() {
        return upperX;
    }

    public int getUpperY() {
        return upperY;
    }


}
